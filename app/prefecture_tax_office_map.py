def get_pref_tax_office_name(prefecture: str, city: str) -> str | None:
    """
    都道府県名と市区町村名から都道府県税事務所の正式名称を返す。
    見つからない場合は None を返す。

    Args:
        prefecture: 都道府県名（例: "東京都", "大阪府", "神奈川県"）
        city: 市区町村名（例: "新宿区", "大阪市", "横浜市"）

    Returns:
        都道府県税事務所の正式名称、または None
    """
    offices = PREF_TAX_OFFICE_MAP.get(prefecture, [])
    if not offices:
        return None
    # 完全一致で検索
    for office in offices:
        for muni in office["municipalities"]:
            if muni == city:
                return office["name"]
    # 前方一致で検索（市区町村名が郡名を含む場合など）
    for office in offices:
        for muni in office["municipalities"]:
            if city.startswith(muni) or muni.startswith(city):
                return office["name"]
    # 部分一致で検索
    for office in offices:
        for muni in office["municipalities"]:
            if city in muni or muni in city:
                return office["name"]
    return None


def get_pref_tax_office_candidates(prefecture: str, city: str) -> list[dict]:
    """
    都道府県名と市区町村名から候補となる都道府県税事務所のリストを返す。
    完全一致がない場合は部分一致の候補を返す。

    Returns:
        [{"name": "...", "municipalities": [...]}, ...]
    """
    offices = PREF_TAX_OFFICE_MAP.get(prefecture, [])
    if not offices:
        return []
    # 完全一致
    for office in offices:
        if city in office["municipalities"]:
            return [office]
    # 部分一致
    candidates = []
    for office in offices:
        for muni in office["municipalities"]:
            if city in muni or muni in city:
                if office not in candidates:
                    candidates.append(office)
    return candidates
